library(shiny)


###############################################################################

shinyServer(function(input, output) {
   
  output$my_output <- renderUI({
    
    if(is.null(input$arithmetic_input)){return(NULL)}
    
    return(
      as.character(eval(parse(text = as.character(input$arithmetic_input))))
    )
    
  })
  
})


